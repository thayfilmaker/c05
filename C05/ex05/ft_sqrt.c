int	ft_sqrt(int nb)
{
	int	i;
	int	num;

	i = 1;
	num = 0;
	while (nb > 0)
	{
		nb -= i;
		i += 2;
		num++;
	}
	if (nb == 0)
		return (num);
	return (0);
}
